<template>
  <section v-click-outside="closeDropdown">
    <button class="dropdown__toggle" :class="{ 'dropdown__toggle--active': isOpen }" @click="toggleDropdown">
      <slot name="button"/>
    </button>

    <div class="dropdown" :class="{ 'is-open': isOpen }">
      <slot />
    </div>
  </section>
</template>

<script>
export default {
  name: 'Dropdown',
  data () {
    return {
      isOpen: false
    };
  },
  methods: {
    toggleDropdown () {
      this.isOpen = !this.isOpen;
    },
    closeDropdown () {
      this.isOpen = false;
    }
  }
};
</script>

